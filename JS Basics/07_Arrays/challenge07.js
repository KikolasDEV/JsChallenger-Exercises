/*
    Find the Length of a Movie List
    You have a list of your favorite movies. Store the number of movies in a variable named 'movieCount'.

    const movies = ['Inception', 'Coco', 'The Matrix', 'Spirited Away'];
    // Store the number of movies in movieCount
    
    console.log(movieCount); // should print 4
    console.log(movieCount === 4);
*/

const movies = ['Inception', 'Coco', 'The Matrix', 'Spirited Away'];
const movieCount = movies.length;
console.log(movieCount); // should print 4
console.log(movieCount === 4);
 