/*
    Shorten arrow function
    Shorten this arrow function as much as possible.

    const func = (a) => {
      const multiplier = 2;
      return a * multiplier;
    }
    
    console.log(func(10) === 20);
    console.log(func(4) === 8);
*/

const func = a => a * 2;
  
console.log(func(10) === 20);
console.log(func(4) === 8);